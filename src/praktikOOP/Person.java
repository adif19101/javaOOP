public class Person {
    private String id;
    private String nama;
    private int Umur;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    /**
     * @return the Umur
     */
    public int getUmur() {
        return Umur;
    }

    /**
     * @param Umur the Umur to set
     */
    public void setUmur(int Umur) {
        this.Umur = Umur;
    }
}
